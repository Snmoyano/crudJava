    /**
     * Button 1 - Aceptar/OK
     * Este boton envia la informacion para que se vea reflejada en la tabla.
     * @param evt 
     */
    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:        Lista ls=new Lista();

        if (this.edita) {
            this.udata();
        } else {
            String nombre = tnombre.getText();
            String edad = tedad.getText();
            Alumnos al = new Alumnos(nombre, Integer.parseInt(edad));
            db.add(al);

        }
        this.adddata();
        this.bdato();

    }//GEN-LAST:event_jButton1ActionPerformed
    /**
     * Button 2 - Editar/Update
     * Este boton nos permite editar nuestra informacion , es decir nos permite cambiar el ,
     * Nombre y la Edad y que luego la informacion se vea reflejada en la tabla
     * @param evt 
     */
    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        int se = tdata.getSelectedRow();
        if (se >= 0) {
            Alumnos al = (Alumnos) db.get(se);
            tnombre.setText(al.getNombre());
            tedad.setText(String.valueOf(al.getEdad()));
            this.edita = true;
        }

    }//GEN-LAST:event_jButton2ActionPerformed
    /**
     * Button 3 - Eliminar / Delete
     * Este boton nos permite eliminar la fila seleccionada.
     * @param evt 
     */
    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        // TODO add your handling code here:
        int se = tdata.getSelectedRow();
        if (se >= 0) {
            db.remove(se);
            this.adddata();
        }
        this.adddata();

    }//GEN-LAST:event_jButton3ActionPerformed

