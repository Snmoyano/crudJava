/**
 * Este Metodo nos ayuda a agragar informacion a la tabla.
 */
    public void adddata() {
        DefaultTableModel ta = (DefaultTableModel) tdata.getModel();
        while (ta.getRowCount() > 0) {
            ta.removeRow(0);
        }
        for (Iterator iterator = db.iterator(); iterator.hasNext();) {
            Alumnos al = (Alumnos) iterator.next();
            Object[] ob = new Object[2];
            ob[0] = al.getNombre();
            ob[1] = al.getEdad();
            ta.addRow(ob);
        }

    }

    /**
     *Este metodo nos permite setear el nombre y la edad a "vacio".
     */
    public void bdato() {
        tnombre.setText("");
        tedad.setText("");
        this.edita = false;
    }

    /**
     *Este metodo nos permite actualizar los datos.
     */
    public void udata() {
        int se = tdata.getSelectedRow();
        if (se >= 0) {
            Alumnos al = new Alumnos(tnombre.getText(), Integer.parseInt(tedad.getText()));
            db.set(se, al);
        }

    }

